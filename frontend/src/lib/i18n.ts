export type TranslationKey = string

export function translate(lang: Lang, key: TranslationKey, values?: Record<string, string | number>): string {
  const template = TEXTS[lang][key] ?? TEXTS.es[key]
  if (!values) {
    return template
  }

  return Object.entries(values).reduce(
    (message, [token, value]) => message.replaceAll(`{{${token}}}`, String(value)),
    template
  )
}

export function useI18n() {
  const lang = useStore((state) => state.lang)
  const setLang = useStore((state) => state.setLang)

  return {
    lang,
    setLang,
    t: (key: TranslationKey, values?: Record<string, string | number>) => translate(lang, key, values)
  }
}
